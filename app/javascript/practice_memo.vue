<template lang="pug">
.a-card
  .card-body(v-if='!editing')
    .card__description(v-if='memo')
      .a-long-text.is-md(v-html='markdownMemo')
    .card-list(v-else)
      .card-list__message
        .container
          .o-empty-message
            .o-empty-message__icon
              i.fa-regular.fa-sad-tear
            .o-empty-message__text
              | プラクティスメモはまだありません。
    footer.card-footer
      .card-main-actions
        ul.card-main-actions__items
          li.card-main-actions__item
            button.card-main-actions__action.a-button.is-sm.is-secondary.is-block(
              @click='editMemo')
              i.fa-solid.fa-pen
              | 編集
  .card-body(v-show='editing')
    .form-tabs.js-tabs
      .form-tabs__tab.js-tabs__tab(
        :class='{ "is-active": isActive("memo") }',
        @click='changeActiveTab("memo")')
        | メモ
      .form-tabs__tab.js-tabs__tab(
        :class='{ "is-active": isActive("preview") }',
        @click='changeActiveTab("preview")')
        | プレビュー
    .a-markdown-input.js-markdown-parent
      .a-markdown-input__inner.is-editor.js-tabs__content(
        :class='{ "is-active": isActive("memo") }')
        textarea.a-text-input.a-markdown-input__textarea(
          :id='`js-practice-memo`',
          data-preview='#practice-memo-preview',
          v-model='memo',
          name='practice[memo]')
      .a-markdown-input__inner.is-preview.js-tabs__content(
        :class='{ "is-active": isActive("preview") }')
        .a-long-text.a-markdown-input__preview(v-html='markdownMemo')
    .card-footer
      .card-main-actions
        .card-main-actions__items
          .card-main-actions__item
            button.a-button.is-sm.is-primary.is-block(@click='updateMemo')
              | 保存する
          .card-main-actions__item
            button.a-button.is-sm.is-secondary.is-block(@click='cancel')
              | キャンセル
</template>
<script>
import CSRF from 'csrf'
import TextareaInitializer from 'textarea-initializer'
import MarkdownInitializer from 'markdown-initializer'
import confirmUnload from 'confirm-unload'
import toast from 'toast'

export default {
  mixins: [confirmUnload, toast],
  props: {
    practiceId: { type: String, required: true }
  },
  data() {
    return {
      memo: '',
      tab: 'memo',
      editing: false
    }
  },
  computed: {
    markdownMemo() {
      const markdownInitializer = new MarkdownInitializer()
      return markdownInitializer.render(this.memo)
    }
  },
  created() {
    fetch(`/api/practices/${this.practiceId}.json`, {
      method: 'GET',
      headers: {
        'X-Requested-With': 'XMLHttpRequest'
      },
      credentials: 'same-origin',
      redirect: 'manual'
    })
      .then((response) => {
        return response.json()
      })
      .then((json) => {
        if (json.memo) {
          this.memo = json.memo
        }
      })
      .catch((error) => {
        console.warn(error)
      })
  },
  mounted() {
    TextareaInitializer.initialize('#js-practice-memo')
  },
  methods: {
    isActive(tab) {
      return this.tab === tab
    },
    changeActiveTab(tab) {
      this.tab = tab
    },
    cancel() {
      fetch(`/api/practices/${this.practiceId}.json`, {
        method: 'GET',
        headers: {
          'X-Requested-With': 'XMLHttpRequest'
        },
        credentials: 'same-origin',
        redirect: 'manual'
      })
        .then((response) => {
          return response.json()
        })
        .then((json) => {
          this.memo = json.memo
        })
        .catch((error) => {
          console.warn(error)
        })
      this.editing = false
    },
    updateMemo() {
      const params = {
        practice: {
          memo: this.memo
        }
      }
      fetch(`/api/practices/${this.practiceId}`, {
        method: 'PUT',
        headers: {
          'Content-Type': 'application/json; charset=utf-8',
          'X-Requested-With': 'XMLHttpRequest',
          'X-CSRF-Token': CSRF.getToken()
        },
        credentials: 'same-origin',
        redirect: 'manual',
        body: JSON.stringify(params)
      })
        .then(() => {
          this.editing = false
          this.toast('保存しました！')
        })
        .catch((error) => {
          console.warn(error)
        })
    },
    editMemo() {
      this.editing = true
    }
  }
}
</script>
